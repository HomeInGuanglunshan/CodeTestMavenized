package integerTest.p04.highestOneBit;

public class IntegerTest {

	public static void main(String[] args) {
		System.out.println(Integer.highestOneBit(15)); // 00001111
		System.out.println(Integer.highestOneBit(18)); // 00010010
		System.out.println(Integer.highestOneBit(23)); // 00010111
		System.out.println(Integer.highestOneBit(33)); // 00100001
		
//		以i = 18为例
//		00000000 00000000 00000000 00010010 = 18
//		i |= (i >>  1);
//		00000000 00000000 00000000 00001001
//		00000000 00000000 00000000 00011011
//		i |= (i >>  2);
//		00000000 00000000 00000000 00000110
//		00000000 00000000 00000000 00011111
//		i |= (i >>  4);
//		00000000 00000000 00000000 00000001
//		00000000 00000000 00000000 00011111
//		i |= (i >>  8);
//		00000000 00000000 00000000 00000000
//		00000000 00000000 00000000 00011111
//		i |= (i >> 16);
//		00000000 00000000 00000000 00000000
//		00000000 00000000 00000000 00011111
//		i - (i >>> 1);
//		00000000 00000000 00000000 00001111
//		00000000 00000000 00000000 00010000 = 16
	}
}
