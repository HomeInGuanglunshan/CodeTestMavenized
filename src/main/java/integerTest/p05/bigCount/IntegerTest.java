package integerTest.p05.bigCount;

public class IntegerTest {

	public static void main(String[] args) {
		System.out.println(Integer.bitCount(23));
		System.out.println(Integer.bitCount(18));
		System.out.println(Integer.bitCount(7));
		System.out.println(Integer.bitCount(-1));
	}

//	第一步：i = i - ((i >>> 1) & 0x55555555);
//	以i = 23，begin
//	00000000 00000000 00000000 00010111
//	>>> 1
//	00000000 00000000 00000000 00001011
//	& 0x55555555
//	01010101 01010101 01010101 01010101
//	00000000 00000000 00000000 00000001
//	i -
//	00000000 00000000 00000000 00010110
//
//	第二步：i = (i & 0x33333333) + ((i >>> 2) & 0x33333333);
//	result2.1 = i & 0x33333333
//	00110011 00110011 00110011 00110011
//	00000000 00000000 00000000 00010010
//	result2.2 = (i >>> 2) & 0x33333333
//	i >>> 2
//	00000000 00000000 00000000 00000101
//	& 0x33333333
//	00110011 00110011 00110011 00110011
//	00000000 00000000 00000000 00000001
//	result2.1 + result2.2
//	00000000 00000000 00000000 00010011
//
//	第三步：i = (i + (i >>> 4)) & 0x0f0f0f0f;
//	i >>> 4
//	00000000 00000000 00000000 00000001
//	i +
//	00000000 00000000 00000000 00010100
//	& 0x0f0f0f0f
//	00001111 00001111 00001111 00001111
//	00000000 00000000 00000000 00000100
//
//	第四步：i = i + (i >>> 8);
//	i >>> 8
//	00000000 00000000 00000000 00000000
//	i +
//	00000000 00000000 00000000 00000100
//
//	第五步：i = i + (i >>> 16);
//	i >>> 16
//	00000000 00000000 00000000 00000000
//	i +
//	00000000 00000000 00000000 00000100
//
//	第六步：return i & 0x3f;
//	& 0x3f
//	00000000 00000000 00000000 00111111
//	00000000 00000000 00000000 00000100
}
